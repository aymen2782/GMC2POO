    }

    public function muet() {
        $this->son = 0;
    }

    public function augmenterSon() {
        if ($this->son < self::SON_MAXIMAL)
               $this->son++;
    }

    public function diminuerSon() {
        if ($this->son > self::SON_MINIMAL)
               $this->son--;
    }

