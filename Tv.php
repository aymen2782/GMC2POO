    const SON_MAXIMAL = 10;
   const SON_MINIMAL = 0;
   const TVA = 18;
   const MIN_PRICE = 5;
   public function __construct(string $nom = "unknown", int $ns = 000000, string $marque = "LG", int $prix = 200, $type="LED",$resolution="FHD")
   {
       parent::__construct($nom, $ns, $marque, $prix);
       $this->resolution = $resolution;
       $this->type = $type;
       $this->son = 5;
       self::$nbVendu++;
   }

    /**
     * @return mixed
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @param mixed $type
     */
